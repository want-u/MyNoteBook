public class BinaryTest {
	public static void main(String[] args) {
		// n1 二进制
		int n1 = 0b1010; // 10
		// n2 10进制
		int n2 = 1010; // 1010
		// n3 8进制
		int n3 = 01010; // 520
		// n4 16进制
		int n4 = 0X1010; // 4112

		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);
		System.out.println(n4);
	}
}